
package progra2;

public class ExpresionesvsInstrucciones {
    
    public static void main(String[] args) {
    int x = 10;          // Línea 1
    x = x + 5;           // Línea 2
    System.out.println(x); // Línea 3
    
//Expresiones: 10, x + 5, x dentro del println → producen valores.
//Instrucciones: toda la línea con ; que hace algo: declaración/asignación/impresión (líneas 1, 2 y 3).
//Diferencia: la expresión calcula un valor; mientras que la instrucción realiza una acción (puede contener expresiones).
 }
}
